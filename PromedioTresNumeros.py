# Escribe un programa que solicite al usuario ingresar tres numeros
# para luego mostrale el promedio de los tres. 

N1 = int(input("Ingrese el primer mumero: "))
N2 = int(input("Ingrese el segundo mumero: "))
N3 = int(input("Ingrese el tercer mumero: "))
R = N1 + N2 + N3
P = R / 3

print("El resultado de la suma es:" , R)
print("El promedio es:" , P)